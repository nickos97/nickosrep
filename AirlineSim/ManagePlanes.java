         DefaultTableModel tm = (DefaultTableModel)table1.getModel();
         tm.setRowCount(0);
         
         while(rs.next()){
             
             Object[] o = {rs.getInt("AIRPLANE_ID"), rs.getString("MODEL"), rs.getString("SIZE")};
         
             
             
             
             tm.addRow(o);
         }
